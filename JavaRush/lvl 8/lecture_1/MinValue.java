// primitive problem from JavaRush
// initialize all variable types with min value
public class MinValue {

    public static void main(String[] args) {

        // -128
        byte bYte = Byte.MIN_VALUE;
        // -32768
        short sHort = Short.MIN_VALUE;
        // -2147483648
        int iNt = Integer.MIN_VALUE;
        // -9223372036854775808
        long loNg = Long.MIN_VALUE;
    }
}
