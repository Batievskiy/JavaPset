// simple problem from JavaRush
// calculate a perimeter and square of a circle
// use final variable Pi
public class PiGeometry {
    public static final double pi = 3.14;
    public static double radius = 2.5;
    public static double diameter = 5;

    public static void main(String[] args) {
        System.out.println("square of a circle is : " + pi * radius * radius);
        System.out.println("perimeter of a circle is : " + pi * diameter);
    }
}